    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String capturaPantalla=txtNombre.getText().trim();
        if(!capturaPantalla.equals("")){
            cmbNombres.addItem(txtNombre.getText());
            modeloElemAgregados.addElement(txtNombre.getText());
            lstElemAgregados.setModel(modeloElemAgregados);
            txtNombre.setText("");
            actualizarCantCmbNombres();
            cmbNombres.setSelectedIndex(-1);
        }
        txtNombre.requestFocus();
    }//GEN-LAST:event_btnAgregarActionPerformed

