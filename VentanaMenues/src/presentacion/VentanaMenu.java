    private void btnBorrarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarItemActionPerformed
        int index=cmbNombres.getSelectedIndex();
        if(index>-1){
            String nomBorrdo=(String)cmbNombres.getSelectedItem();
            cmbNombres.removeItemAt(index);
            modeloElemAgregados.remove(index);
            actualizarCantCmbNombres();
            lstElemAgregados.setModel(modeloElemAgregados);
            modeloElemBorrados.addElement(nomBorrdo);
            lstElemBorrados.setModel(modeloElemBorrados);
            actualizarCantlstElemBorrados();
            cmbNombres.setSelectedIndex(-1);
        }
    }//GEN-LAST:event_btnBorrarItemActionPerformed

