    private void btnBorrarTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarTodosActionPerformed
        int cant=cmbNombres.getModel().getSize();
        String nomBorradoI;
        for(int i=0;i<cant;i++){
            nomBorradoI=cmbNombres.getItemAt(i);
            modeloElemBorrados.addElement(nomBorradoI);
        }
        cmbNombres.removeAllItems();
        modeloElemAgregados.removeAllElements();
        lstElemAgregados.setModel(modeloElemAgregados);
        actualizarCantCmbNombres();
        lstElemBorrados.setModel(modeloElemBorrados);
        actualizarCantlstElemBorrados();
    }//GEN-LAST:event_btnBorrarTodosActionPerformed

